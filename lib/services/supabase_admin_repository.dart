  @override
  Future<void> setEmployerBan(
    String employerId, {
    required bool isBanned,
    String? reason,
    String? companyName,
  }) async {
    var deletedJobListingCount = 0;
    var deletedApplicationCount = 0;

    if (isBanned) {
      final isAdminOwnedEmployer = await _isAdminOwnedEmployer(employerId);
      if (isAdminOwnedEmployer) {
        throw StateError('Cannot ban an admin employer profile.');
      }

      final listingRows = await _client
          .from('job_listings')
          .select('id')
          .eq('employer_id', employerId);
      final listingIds = listingRows
          .map((row) => row['id']?.toString() ?? '')
          .where((id) => id.isNotEmpty)
          .toList();
      deletedJobListingCount = listingIds.length;

      final applicationRows = await _client
          .from('job_applications')
          .select('id')
          .eq('employer_id', employerId);
      deletedApplicationCount = applicationRows.length;

      // Deleting listings will cascade to related applications.
      await _client.from('job_listings').delete().eq('employer_id', employerId);

      for (final listingId in listingIds) {
        await _client
            .from('job_listing_reports')
            .update({
              'status': JobListingReport.statusDeleted,
              'reviewed_at': DateTime.now().toIso8601String(),
              'reviewed_by_admin_user_id': _adminUserId,
              'admin_notes':
                  'Listing deleted due to employer ban: ${reason ?? ''}',
            })
            .eq('job_listing_id', listingId)
            .eq('status', JobListingReport.statusOpen);
      }
    }

    final beforeRow = await _client
        .from('employer_moderation')
        .select()
        .eq('employer_id', employerId)
        .maybeSingle();

    final previousDeletedJobCount =
        ((beforeRow?['admin_deleted_job_count'] as num?)?.toInt() ?? 0);

    final nextRow = {
      'employer_id': employerId,
      if (companyName != null) 'company_name': companyName,
      'admin_deleted_job_count':
          previousDeletedJobCount + deletedJobListingCount,
      'is_banned': isBanned,
      'banned_at': isBanned ? DateTime.now().toIso8601String() : null,
      'ban_reason': isBanned ? (reason ?? '') : '',
    };

    await _client.from('employer_moderation').upsert(nextRow);

    await logAdminAction(
      AdminActionLog(
        id: _pendingId,
        adminUserId: _adminUserId,
        actionType: AdminActionLog.actionUpdate,
        resourceType: AdminActionLog.resourceEmployerProfile,
        resourceId: employerId,
        changesBefore: beforeRow == null
            ? null
            : Map<String, dynamic>.from(beforeRow),
        changesAfter: nextRow,
        reason: isBanned
            ? 'Employer banned${(reason ?? '').trim().isEmpty ? '' : ': $reason'}'
            : 'Employer unbanned',
        timestamp: DateTime.now(),
      ),
    );
  }

  @override
  Future<void> setJobSeekerBan(
    String userId, {
    required bool isBanned,
    String? reason,
    String? displayName,
    String? email,
  }) async {
    var deletedApplicationCount = 0;

    if (isBanned) {
      final isAdminUser = await _isAdminUser(userId);
      if (isAdminUser) {
        throw StateError('Cannot ban an admin job seeker profile.');
      }

      final applicationRows = await _client
          .from('job_applications')
          .select('id')
          .eq('applicant_user_id', userId);
      deletedApplicationCount = applicationRows.length;

      await _client
          .from('job_applications')
          .delete()
          .eq('applicant_user_id', userId);
    }

    final beforeRow = await _client
        .from('job_seeker_moderation')
        .select()
        .eq('user_id', userId)
        .maybeSingle();

    final previousDeletedApplicationCount =
        ((beforeRow?['admin_deleted_application_count'] as num?)?.toInt() ?? 0);

    final nextRow = {
      'user_id': userId,
      if (displayName != null) 'display_name': displayName,
      if (email != null) 'email': email,
      'admin_deleted_application_count':
          previousDeletedApplicationCount + deletedApplicationCount,
      'is_banned': isBanned,
      'banned_at': isBanned ? DateTime.now().toIso8601String() : null,
      'ban_reason': isBanned ? (reason ?? '') : '',
    };

    await _client.from('job_seeker_moderation').upsert(nextRow);

    await logAdminAction(
      AdminActionLog(
        id: _pendingId,
        adminUserId: _adminUserId,
        actionType: AdminActionLog.actionUpdate,
        resourceType: AdminActionLog.resourceJobSeekerProfile,
        resourceId: userId,
        changesBefore: beforeRow == null
            ? null
            : Map<String, dynamic>.from(beforeRow),
        changesAfter: nextRow,
        reason: isBanned
            ? 'Job seeker banned${(reason ?? '').trim().isEmpty ? '' : ': $reason'}'
            : 'Job seeker unbanned',
        timestamp: DateTime.now(),
      ),
    );
  }

  Application _fromApplicationRow(Map<String, dynamic> row) {
    final data = Map<String, dynamic>.from((row['data'] as Map?) ?? const {});
    data['id'] = row['id'] ?? data['id'];
    data['job_listing_id'] = row['job_listing_id'] ?? data['job_listing_id'];
    data['employer_id'] = row['employer_id'] ?? data['employer_id'];
    data['status'] = row['status'] ?? data['status'];
    data['matchPercentage'] =
        row['match_percentage'] ?? data['matchPercentage'];
    data['updated_at'] = row['updated_at'] ?? data['updated_at'];
    data['applied_at'] = row['created_at'] ?? data['applied_at'];
    return Application.fromJson(data);
  }

