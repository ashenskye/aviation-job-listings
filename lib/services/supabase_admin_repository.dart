  // ── Delete ────────────────────────────────────────────────────────────────

  @override
  Future<void> deleteApplication(String appId, String reason) async {
    final beforeRow = await _client
        .from('job_applications')
        .select()
        .eq('id', appId)
        .maybeSingle();

    final currentData = Map<String, dynamic>.from(
      (beforeRow?['data'] as Map?) ?? const {},
    );
    currentData['is_archived'] = true;
    currentData['updated_at'] = DateTime.now().toIso8601String();

    await _client
        .from('job_applications')
        .update({
          'data': currentData,
          'updated_at': DateTime.now().toIso8601String(),
        })
        .eq('id', appId);

    final seekerId = beforeRow?['applicant_user_id']?.toString();
    if (seekerId != null && seekerId.isNotEmpty) {
      await _incrementJobSeekerDeletedApplicationCount(
        userId: seekerId,
        displayName: currentData['applicant_name']?.toString() ?? '',
        email: currentData['applicant_email']?.toString() ?? '',
      );
    }

    await logAdminAction(
      AdminActionLog(
        id: _pendingId,
        adminUserId: _adminUserId,
        actionType: AdminActionLog.actionDelete,
        resourceType: AdminActionLog.resourceApplication,
        resourceId: appId,
        changesBefore: beforeRow != null
            ? Map<String, dynamic>.from(beforeRow)
            : null,
        reason: reason,
        timestamp: DateTime.now(),
      ),
    );
  }

  @override
  Future<void> deleteJobListing(String jobId, String reason) async {
    final beforeRow = await _client
        .from('job_listings')
        .select()
        .eq('id', jobId)
        .maybeSingle();

    // Soft delete: mark status as 'archived' (matches the DB status enum).
    await _client
        .from('job_listings')
        .update({'status': 'archived'})
        .eq('id', jobId);

    final employerId = beforeRow?['employer_id']?.toString();
    if (employerId != null && employerId.isNotEmpty) {
      await _incrementEmployerDeletedJobCount(
        employerId: employerId,
        companyName: beforeRow?['company']?.toString() ?? '',
      );
    }

    await _client
        .from('job_listing_reports')
        .update({
          'status': JobListingReport.statusDeleted,
          'reviewed_at': DateTime.now().toIso8601String(),
          'reviewed_by_admin_user_id': _adminUserId,
          'admin_notes': reason,
        })
        .eq('job_listing_id', jobId)
        .eq('status', JobListingReport.statusOpen);

    await logAdminAction(
      AdminActionLog(
        id: _pendingId,
        adminUserId: _adminUserId,
        actionType: AdminActionLog.actionDelete,
        resourceType: AdminActionLog.resourceJobListing,
        resourceId: jobId,
        changesBefore: beforeRow != null
            ? Map<String, dynamic>.from(beforeRow)
            : null,
        reason: reason,
        timestamp: DateTime.now(),
      ),
    );
  }

  @override
  Future<void> deleteEmployerProfile(String employerId, String reason) async {
    final isAdminOwnedEmployer = await _isAdminOwnedEmployer(employerId);
    if (isAdminOwnedEmployer) {
      throw StateError('Cannot delete an admin employer profile.');
    }

    final beforeRow = await _client
        .from('employer_profiles')
        .select()
        .eq('id', employerId)
        .maybeSingle();

    final listingRows = await _client
        .from('job_listings')
        .select('id')
        .eq('employer_id', employerId);
    final listingIds = listingRows
        .map((row) => row['id']?.toString() ?? '')
        .where((id) => id.isNotEmpty)
        .toList();

    // Hard-delete all listings owned by this employer before deleting profile.
    await _client.from('job_listings').delete().eq('employer_id', employerId);

    for (final listingId in listingIds) {
      await _client
          .from('job_listing_reports')
          .update({
            'status': JobListingReport.statusDeleted,
            'reviewed_at': DateTime.now().toIso8601String(),
            'reviewed_by_admin_user_id': _adminUserId,
            'admin_notes':
                'Listing deleted due to employer profile deletion: $reason',
          })
          .eq('job_listing_id', listingId)
          .eq('status', JobListingReport.statusOpen);
    }

    await _client.from('employer_profiles').delete().eq('id', employerId);

    await logAdminAction(
      AdminActionLog(
        id: _pendingId,
        adminUserId: _adminUserId,
        actionType: AdminActionLog.actionDelete,
        resourceType: AdminActionLog.resourceEmployerProfile,
        resourceId: employerId,
        changesBefore: beforeRow != null
            ? Map<String, dynamic>.from(beforeRow)
            : null,
        changesAfter: {'deleted_job_listing_count': listingIds.length},
        reason: reason,
        timestamp: DateTime.now(),
      ),
    );
  }

